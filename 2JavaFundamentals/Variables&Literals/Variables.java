public class Variables {
    public static void main(String[] args) {
        // Declare a variable of type int
        int myNumber;
        // Assign a value to the variable
        myNumber = 5;
        // Print the value of the variable
        System.out.println(myNumber); // Output: 5

        // Declare a variable of type double
        double myDouble = 5.99;
        // Print the value of the variable
        System.out.println(myDouble); // Output: 5.99

        // Declare a variable of type char
        char myLetter = 'D';
        // Print the value of the variable
        System.out.println(myLetter); // Output: D

        // Declare a variable of type boolean
        boolean myBool = true;
        // Print the value of the variable
        System.out.println(myBool); // Output: true

        // Declare a variable of type String
        String myText = "Hello";
    }
}
